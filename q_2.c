#include <stdio.h>

int main() {
    int N;  // Declare an integer to store the input value N
    
    // Take input for N
    scanf("%d", &N);  // Read the integer N from the user
    
    // Use a loop to print "I Love Practice" N times
    for (int i = 0; i < N; i++) {
        printf("I Love Practice\n");  // Print the phrase followed by a new line
    }
    
    return 0;  // End of the program
}
